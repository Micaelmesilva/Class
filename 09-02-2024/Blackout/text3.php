                The two men got out of the car and headed towards the door.<br><br>
                — Hello! We came to deliver a package — said one of the men.<br><br>
                No response.<br><br>
                — They must be very rich — said one of the men while looking through the window. — Look at that table.<br><br>
                Just as the man moved towards the window to get a better look at the table, a man — around 63 years old — could be seen sitting on the sofa, looking in their direction. The moment their gazes met, each was able to see the other’s soul. For some reason, the delivery man saw in the older man's eyes a feeling that had not been directed at him for a long time: pity. The old man showed pity while shaking his head disapprovingly.<br><br>
                — Man, let’s get out of here! Let someone else deliver it tomorrow. — he said in a desperate tone, as he headed back towards the door. — This place is scaring me. Did you stop to think who opened the gate?<br><br>
                — Don’t worry, we’re here now, and the gate should be automatic.<br><br>
                — Let’s go, please... — The man was interrupted by a woman; her long black hair fell over her shoulders, her black dress accentuated her pale and delicate white skin, and her lips were red. Everything was so balanced and perfect; she was simply beautiful, perhaps one of the most beautiful women they had ever seen.<br><br>
                — Good evening, we came to deliver this package — said the man, handing the package to her. When lightning struck, there was a brief power outage.<br><br>
                — Thank you very much, would you like to come in and wait for the rain to pass? — asked the woman with a sympathetic face.<br><br>
                — That’s very kind of you, I believe that... — The man was interrupted by his frightened friend.<br><br>
                — No, we have to go, it’s late. But don’t worry, we have a car. — said the scared man, pulling his friend towards the rain.
